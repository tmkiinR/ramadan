package com.ramadan.azkar.utilities;


public class Data {
    String subject,link;

    public Data(String subject, String link) {
        this.subject = subject;
        this.link = link;
    }


    public String getSubject() {
        return this.subject;
    }

    public String getLink() {
        return this.link;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public void setLink(String link) {
        this.link = link;
    }
}



